import re

def normalize_text(text: str) -> str:
    """
    Нормализует текст:
    1. Приводит к нижнему регистру.
    2. Заменяет пунктуацию на пробелы (чтобы не слипались слова).
    3. Удаляет лишние пробелы и переносы строк, оставляя одиночные пробелы.
    """
    if not text:
        return ""

    # 1. Lowercase
    text = text.lower()

    # 2. Пунктуацию заменить на пробел (оставляем только буквы, цифры и пробельные символы)
    # [^\w\s] означает "всё, что не буква/цифра и не пробел"
    text = re.sub(r'[^\w\s]', ' ', text)

    # 3. Привести пробелы и переносы к одному виду (один пробел)
    text = re.sub(r'\s+', ' ', text)

    return text.strip()